public class ArithmeticOperations{
    public static void main(String args[]){
        int num1 = 10;
        int num2 = 2;

        //+ Addition
        System.out.println(num1+num2);

        //subtraction
        System.out.println(num1-num2);

        //Multiplication
        System.out.println(num1*num2);

        //Division
        System.out.println(num1/num2);

        //Modular Division
        System.out.println(num1%num2);

        int incremented = --num1;

        int incrementedtwo = ++num1;

        System.out.println(incremented);
        System.out.println(incrementedtwo);
    }
}